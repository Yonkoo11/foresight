async function replaceInfluencers() {
  try {
    console.log('🗑️  Deleting all existing influencers...');
    await knex('influencers').del();

    console.log('✨ Inserting correct CT influencer list...');
    for (const inf of CORRECT_INFLUENCERS) {
      await knex('influencers').insert({
        display_name: inf.name,
        twitter_handle: inf.handle,
        avatar_url: inf.profile_image_url,
        bio: `Crypto Twitter Influencer`,
        tier: inf.tier,
        price: inf.price,
        base_price: inf.base_price,
        follower_count: inf.follower_count,
        engagement_rate: 3.5,
        form_score: 85,
        total_points: 0,
        is_active: true,
      });
    }

    console.log(`✅ Successfully replaced with ${CORRECT_INFLUENCERS.length} real CT influencers!`);
    console.log('📊 Distribution:');
    console.log(`   - S-Tier (45 pts): 10 influencers`);
    console.log(`   - A-Tier (35 pts): 15 influencers`);
    console.log(`   - B-Tier (25 pts): 15 influencers`);
    console.log(`   - C-Tier (15 pts): 10 influencers`);
    console.log('   Total: 50 influencers');

    await knex.destroy();
  } catch (error) {
    console.error('❌ Error replacing influencers:', error);
    await knex.destroy();
    process.exit(1);
  }
}

